# Задаем значение k
k <- 5

# Создаем пустой вектор для хранения значений P
P <- c()

# Для каждого значения j от -1 до k вычисляем значение P по формуле
for (j in -1:k) {
  # Вычисляем сумму по i от j до k + 3
  # Для этого используем функцию sum, которая возвращает сумму элементов вектора
  # В качестве вектора используем анонимную функцию, которая вычисляет выражение (abs(i - 5))^(1/4) / abs(i - 7) для каждого i от j до k + 3
  # Для этого используем функцию sapply, которая применяет функцию к каждому элементу вектора и возвращает вектор результатов
  sum_i <- sum(sapply(j:(k + 3), function(i) (abs(i - 5))^(1/4) / abs(i - 7)))

  # Вычисляем произведение по j
  # Для этого используем функцию prod, которая возвращает произведение элементов вектора
  # В качестве вектора используем выражение (j - j^2) * j / (j + 14)
  prod_j <- prod((j - j^2) * j / (j + 14))

  # Вычисляем значение P как произведение prod_j и sum_i
  P[j + 2] <- prod_j * sum_i
}

# Выводим вектор P на экран
print(P)
