# Определяем функцию max_min_prod, которая принимает два вектора x и y в качестве аргументов
max_min_prod <- function(x, y) {
  # Проверяем, что x и y имеют одинаковую длину
  if (length(x) != length(y)) {
    # Если нет, то выводим сообщение об ошибке и останавливаем выполнение функции
    stop("Векторы должны иметь одинаковую длину")
  }

  # Вычисляем произведение векторов x и y поэлементно
  # Для этого используем оператор *
  z <- x * y

  # Находим максимальное и минимальное значение вектора z
  # Для этого используем функции max и min
  max_z <- max(z)
  min_z <- min(z)

  # Возвращаем вектор из максимального и минимального значения вектора z
  # Для этого используем функцию c
  c(max_z, min_z)
}

# Пример использования функции max_min_prod
# Создаем два вектора x и y из случайных чисел от 1 до 10
x <- sample(1:10, 5)
y <- sample(1:10, 5)

# Выводим векторы x и y на экран
print(x)
print(y)

# Вызываем функцию max_min_prod с аргументами x и y
# Выводим результат на экран
print(max_min_prod(x, y))
