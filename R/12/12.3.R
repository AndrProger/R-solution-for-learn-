# Определяем функцию sum_digits, которая принимает один аргумент n
sum_digits <- function(n) {
  # Проверяем, что n является натуральным числом
  if (is.integer(n) || n <= 0) {
    # Если нет, то выводим сообщение об ошибке и останавливаем выполнение функции
    stop("n должно быть натуральным числом")
  }

  # Преобразуем число n в строку с помощью функции as.character
  s <- as.character(n)

  # Разбиваем строку s на отдельные символы с помощью функции strsplit
  # Эта функция возвращает список, поэтому используем двойные квадратные скобки, чтобы получить вектор
  chars <- strsplit(s, "")[[1]]

  # Преобразуем символы в числа с помощью функции as.numeric
  nums <- as.numeric(chars)

  # Вычисляем сумму чисел с помощью функции sum
  sum_nums <- sum(nums)

  # Возвращаем сумму чисел
  sum_nums
}

# Пример использования функции sum_digits
# Задаем значение n
n <- 11

# Вызываем функцию sum_digits с аргументом n
# Выводим результат на экран
print(sum_digits(n))
